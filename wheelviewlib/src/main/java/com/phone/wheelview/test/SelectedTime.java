package com.phone.wheelview.test;

public class SelectedTime {
	private int year;
	private int month;
	private int day;
	private boolean isAm;
	private int hour;
	private int minute;

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public boolean isAm() {
		return isAm;
	}

	public void setAm(boolean isAm) {
		this.isAm = isAm;
	}

	public int getHour() {
		return hour;
	}

	public void setHour(int hour) {
		this.hour = hour;
	}

	public int getMinute() {
		return minute;
	}

	public void setMinute(int minute) {
		this.minute = minute;
	}

}
